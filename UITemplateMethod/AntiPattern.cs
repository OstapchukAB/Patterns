#region Антиппатерн "Шаблонный метод" (Template Method)
/*
 Недостатки:
  Дублирование кода (например, Кипятим воду).
  При изменении общего шага (например, Наливаем в чашку) нужно править все классы.
  Нет гарантии соблюдения порядка шагов.

 **************************
Решение через шаблонный метод
  Выносим общие шаги в базовый класс.
  Уникальные шаги инкапсулируем в абстрактные методы.
Преимущества:

Устранено дублирование.
 - Изменения в общих шагах вносятся в одном месте.
 - Подклассы фокусируются на уникальной логике.
 */
#endregion
namespace UITemplateMethodAntiPattern
{
    public class CoffeeMaker
    {
        public void Prepare()
        {
            Console.WriteLine("Кипятим воду"); // Дублирование
            Console.WriteLine("Завариваем кофе");
            Console.WriteLine("Наливаем в чашку"); // Дублирование
            Console.WriteLine("Добавляем молоко и сахар");
        }
    }

    public class TeaMaker
    {
        public void Prepare()
        {
            Console.WriteLine("Кипятим воду"); // Дублирование
            Console.WriteLine("Завариваем чайные листья");
            Console.WriteLine("Наливаем в чашку"); // Дублирование
            Console.WriteLine("Добавляем лимон");
        }
    }
}
